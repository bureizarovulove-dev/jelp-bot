def get_response(percentage):
    """Получить случайное сообщение для процента"""
    import random
    
    for (min_p, max_p), messages in RESPONSES.items():
        if min_p <= percentage <= max_p:
            return random.choice(messages).format(percentage)
    
    return f"jelp {percentage}% — Неизвестное состояние"
